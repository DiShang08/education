# Задан целочисленный двумерный массив, состоящий из N строк и M столбцов.
# Требуется вычислить сумму элементов в каждой строке и в каждом столбце.
#
# Программа получает на вход два натуральных числа N и M – количество строк
# и столбцов двумерного массива. В каждой из последующих N строк записаны
# M целых чисел – элементы массива. Все числа во входных данных не превышают
# 1000 по абсолютной величине.
#
# В первой строке вам необходимо вывести N чисел – суммы элементов массива
# для каждой строки в отдельности.
#
# Во второй строке в аналогичном формате выведите M чисел – суммы элементов
# для каждого столбца.

n, m = map(int, input().split())
a = []
for i in range(n):
    a.append(list(map(int, input().split())))
for i in range(n):
    s_row = 0
    for j in range(m):
        s_row += a[i][j]
    print(s_row, end=' ')
print()
for j in range(m):
    s_col = 0
    for i in range(n):
        s_col += a[i][j]
    print(s_col, end=' ')

