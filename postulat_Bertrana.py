# если не нашлось делителя для числа на промежутке от "2" до "квадратного корня этого числа +1"
# то и дальше его (делитель) смысла нет искать - его там не будет. тогда это
# число - простое. а значит каунт +1.
#
# Для любого не простого числа найдется делитель на промежутке от 2 до квадратного корня
# этого числа. В алгоритме, чтобы корень брался есть +1.
#
# число 28 - его корень 5.3. Проверяется делтся ли 28 на 2/3/4/5.
# видно что оно делится на 2 - значит уже не простое
#
# число 29 - его корень 5.4. Проверяется делится ли 29 на 2/3/4/5.
# Ни одно из этих чисел не делит 29 без остатка.
# Проверять 6/7/8....28 нет смысла. они его делить также не будут.

n = int(input())
a = 0
for i in range(n + 1, n * 2):
    for j in range(2, int(i ** 0.5) + 1):
        if i % j == 0:
            break
    else:
        a += 1
print(a)
